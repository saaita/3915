package ch03_oper;
// += -= *= /= %=
public class Test07 {
	public static void main(String[] args) {
		
		int a=10;
		
		a+=2; // a=a+2
		System.out.println(a);
		
		a-=2; // a=a-2
		System.out.println(a);//10
		
		a*=2; // a=a*2
		System.out.println(a);//20
		
		a/=2; // a=a/2
		System.out.println(a);//10
		
		a%=3; //a=a%3
		System.out.println(a);//1
		
	}//main-end
}//class-end
