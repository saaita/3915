package ch04_control;

public class Test09_for {
	public static void main(String[] args) {
		//"수요일"5번출력
		for(int i=1; i<=5; i++){
			System.out.println("수요일");
		}//for-end
		System.out.println();

		for(int i=1; i<=10; i++){
			System.out.println("강도현");
		}//for-end
		System.out.println();
	}//main-end
}//class-end
