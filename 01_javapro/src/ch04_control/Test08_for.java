package ch04_control;

public class Test08_for {
	public static void main(String[] args) {
		//문제 : 1~10까지 출력
		for(int i=1; i<=10; i++){
			System.out.print(i+" ");

		}//for-end
		System.out.println();
		//============
		//문제 : 10~1까지 출력
		
		for(int i=10; i>=1; i--){
			System.out.print(i+" ");
		}
		System.out.println();
	}//main-end
}//class-end
